export function formatText(template, values = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => values[key] ?? '')
}

export function formatDate(date, language) {
  if (language !== 'zh') return date
  const [month, day] = date.split(' ')
  const monthMap = { Jun: '6月', Jul: '7月' }
  return `${monthMap[month] ?? month}${day}日`
}
